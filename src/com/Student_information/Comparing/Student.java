package com.Student_information.Comparing;
public class Student {
	int id;
	String FirstName;
	double CGPA;
	/**
	 * Constructor to initialize the Student's object
	 * @param id
	 * @param FirstName
	 * @param CGPA
	 */

	Student(int id, String FirstName, double CGPA) {
		this.id = id;
		this.FirstName = FirstName;
		this.CGPA = CGPA;
	}
}